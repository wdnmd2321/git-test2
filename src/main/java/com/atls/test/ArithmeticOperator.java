package com.atls.test;

public class ArithmeticOperator {
    public static void main(String[] args) {
        //演示算术运算符的使用
        System.out.println(10.0/4);
        //注释快捷键 ctrl +/ ,再次输入 ctrl +/取消注释

        double d = 10/4;
        System.out.println(d);
        System.out.println(10 % 3);
        System.out.println(-10 % 3);
        System.out.println(10 % -3);
        System.out.println(-10 % -3);
    }
}
